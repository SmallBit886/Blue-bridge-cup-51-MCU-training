#include "LED.h"
/**
  ************************************************************************** 
  ** -------------------------------------------------------------------- **
  ** @name          : None
  ** @brief         : P2= (0001 1111) | (1000 0000)  
  ** @param         : 右移动0位Y4X，右移动1位Y4X，右移动2位Y4X
  ** @retval        : None
  ** @author        : SmallBit886
  ** -------------------------------------------------------------------- **
  ** @attention     :  Y4 --- 100  Y5 --- 010  Y6 --- 001
  ** -------------------------------------------------------------------- **
  ************************************************************************** 
**/
void YXC(unsigned char YxC)
{
	P2 =(P2 & 0x1f) | (0x80>>YxC);
}
/**
  ************************************************************************** 
  ** -------------------------------------------------------------------- **
  ** @name          : None
  ** @brief         : num为0,L1点亮
  ** @param         : P0=~(0000 0000 | 0000 0001)=1111 1110
  ** @retval        : None
  ** @author        : SmallBit886
  ** -------------------------------------------------------------------- **
  ** @attention     : 非法操作全部熄灭
  ** -------------------------------------------------------------------- **
  ************************************************************************** 
**/

void LED_SHOW(unsigned char num)
{
	if(8 > num && 0 <= num)
	{
		P0 = ~((P0 & 0x00) | (0x01 << num));
	}
	else
	{
		P0 = 0xff;//全部熄灭
	}
}
/***********************************************************/
void LED_Init(void)
{
	LED_ALL_OFF();//熄灭全部
	YXC(0);
}
/***********************************************************/
void LED_ALL_ON(void)
{
	P0 = 0x00;//全部亮
}
/***********************************************************/
void LED_ALL_OFF(void)
{
	P0 = 0xff;//全部熄灭
}
