#include "LED.h"
sbit L1 = P0^0;
void LED_Work(void)
{
	Init_HC138(4);//Y4
	L1=0;
	Delay(1000);
	Init_HC138(4);//Y4
	L1=1;
	Delay(1000);
}
/**
  ************************************************************************** 
  ** -------------------------------------------------------------------- **
  ** @name          : None
  ** @brief         : num为0,L1点亮
  ** @param         : P0=~(0000 0000 | 0000 0001)=1111 1110
  ** @retval        : None
  ** @author        : SmallBit886
  ** -------------------------------------------------------------------- **
  ** @attention     : 非法操作全部熄灭
  ** -------------------------------------------------------------------- **
  ************************************************************************** 
**/

void LED_SHOW(unsigned char num)
{
	if(8 > num && 0 <= num)
	{
		Init_HC138(4);//Y4
		P0 = ~((P0 & 0x00) | (0x01 << num));
	}
	else
	{
		Init_HC138(4);//Y4
		P0 = 0xff;//全部熄灭
	}
}
/***********************************************************/
void LED_Init(void)
{
	Init_HC138(4);//Y4
	LED_ALL_OFF();//熄灭全部
}
/***********************************************************/
void LED_ALL_ON(void)
{
	Init_HC138(4);//Y4
	P0 = 0x00;//全部亮
}
/***********************************************************/
void LED_ALL_OFF(void)
{
	Init_HC138(4);//Y4
	P0 = 0xff;//全部熄灭
}
